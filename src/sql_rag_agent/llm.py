def _repair_sql_prompt(
    *,
    question: str,
    question_analysis: dict[str, Any],
    schema_context: list[dict[str, Any]],
    selected_tables: list[str],
    retrieved_context: list[dict[str, Any]],
    previous_candidate: dict[str, Any],
    execution_error: str,
    retry_count: int,
) -> str:
    schema_summary = _schema_summary(schema_context)
    semantic_summary = _semantic_context_summary(retrieved_context)
    payload = {
        "question": question,
        "question_analysis": question_analysis,
        "selected_tables": selected_tables,
        "previous_sql": previous_candidate.get("sql"),
        "previous_reasoning_summary": previous_candidate.get("reasoning_summary"),
        "execution_error": execution_error,
        "retry_count": retry_count,
    }
    return (
        "Repair the PostgreSQL query after an execution failure.\n"
        "Return one JSON object only, with keys: sql, reasoning_summary, expected_result_shape, filters.\n"
        "Keep the original analytical intent, but fix the SQL so it can execute.\n"
        "Rules:\n"
        "- Output a single read-only SELECT or WITH ... SELECT statement.\n"
        "- Do not use INSERT, UPDATE, DELETE, DROP, ALTER, CREATE, COPY, CALL, or DO.\n"
        "- Use only the listed selected tables and listed columns.\n"
        "- Prefer schema-qualified table names and explicit aliases.\n"
        "- Use the execution error as the primary clue for what to fix.\n"
        "- If a table/column/status value seems semantically wrong, revise it to a likely valid alternative.\n\n"
        f"Repair context: {json.dumps(payload, ensure_ascii=True)}\n"
        f"Semantic context:\n{semantic_summary}\n"
        f"Schema:\n{schema_summary}\n"
    )


def _answer_prompt(
    question: str,
    candidate: dict[str, Any],
    execution_result: dict[str, Any],
    selected_tables: list[str],
) -> str:
    payload = {
        "question": question,
        "selected_tables": selected_tables,
        "sql": candidate.get("sql"),
        "reasoning_summary": candidate.get("reasoning_summary"),
        "filters": candidate.get("filters", []),
        "rows": execution_result.get("rows", []),
        "row_count": execution_result.get("row_count"),
        "error": execution_result.get("error"),
    }
    return (
        "Compose a grounded, natural-language answer from this SQL execution payload. "
        "Include the direct answer, calculation basis, tables used, filters applied, and a caveat if needed. "
        "Do not mention internal trace logs. Do not invent values outside the rows.\n\n"
        f"{json.dumps(payload, ensure_ascii=True, default=str)}"
    )


def _schema_summary(schema_context: list[dict[str, Any]]) -> str:
    lines = []
    for table in schema_context:
        columns = ", ".join(f"{col['name']} ({col['type']})" for col in table.get("columns", []))
        lines.append(f"- {table['table_name']}: {columns}")
        for fk in table.get("foreign_keys", []):
            lines.append(
                "  FK "
                f"{fk['column']} -> {fk['references_table']}.{fk['references_column']}"
            )
    return "\n".join(lines)


def _semantic_context_summary(retrieved_context: list[dict[str, Any]]) -> str:
    if not retrieved_context:
        return "- No semantic context was retrieved."
    lines = []
    for item in retrieved_context:
        content = str(item.get("content") or "").strip()
        if len(content) > 1200:
            content = content[:1200] + "...[truncated]"
        lines.append(
            "- "
            f"{item.get('type', 'context')} {item.get('name', 'unnamed')} "
            f"(score={item.get('score', 0)}, source={item.get('source', '')}): {content}"
        )
    return "\n".join(lines)


def _parse_json_object(content: str) -> dict[str, Any] | None:
    cleaned = content.strip()
    fence_match = re.search(r"```(?:json)?\s*(\{.*?\})\s*```", cleaned, re.DOTALL)
    if fence_match:
        cleaned = fence_match.group(1)
    try:
        parsed = json.loads(cleaned)
    except json.JSONDecodeError:
        return None
    return parsed if isinstance(parsed, dict) else None


def _is_valid_question_analysis(payload: dict[str, Any]) -> bool:
    question_type = payload.get("question_type")
    expected_answer_type = payload.get("expected_answer_type")
    ambiguities = payload.get("ambiguities")
    if question_type not in {"lookup", "ranking", "count", "aggregate", "comparison", "trend", "filter"}:
        return False
    if expected_answer_type not in {"sentence", "number", "ranked_list", "table"}:
        return False
    for key in ("requires_time_filter", "requires_join", "requires_metric_definition"):
        if not isinstance(payload.get(key), bool):
            return False
    if not isinstance(ambiguities, list) or not all(isinstance(item, str) for item in ambiguities):
        return False
    return True
